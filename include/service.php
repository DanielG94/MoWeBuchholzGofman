<h1>Unsere Leistungen</h2>
<p>
  Die Häusliche Pflege Schal & Ke bietet eine Vielzahl qualitativ hochwertiger Dienstleistungen rund um die häusliche Versorgung an.
  Damit Sie in Ihrer vertrauten Umgebung, ihrem Zuhause bleiben und dort weitgehend selbstbestimmt leben können.
</p>
<p>
  Zu diesem Angebot gehören auch Hilfen für die Angehörigen pflegebedürftiger Menschen, die nicht selten entscheidend zur Pflege und
  Betreuung beitragen.
</p>
<p>
  In vielen Fällen entwickelt sich der Hilfe- oder Unterstützungsbedarf langsam und schleichend, und es werden nur einige gezielte
  Hilfen nötig, wie der Wocheneinkauf oder das wöchentliche Bad.
</p>
<p>
  Es gibt jedoch auch Situationen, in denen es nötig ist, einmal oder auch mehrmals täglich Hilfe und Unterstützung in Anspruch zu nehmen.
</p>
<p>
  Gerne helfen wir Ihnen dabei die Unterstützung zu finden, die Sie sich wünschen.
</p>
